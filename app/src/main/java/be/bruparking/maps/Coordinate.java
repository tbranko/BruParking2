package be.bruparking.maps;

public class Coordinate {
	private Double lat;
	private Double lng;
	
	public void setLat(Double lat) {
		this.lat = lat;
	}

	public Double getLat() {
		return this.lat;
	}
	
	public void setLng(Double lng) {
		this.lng = lng;
	}

	public Double getLng() {
		return this.lng;
	}
}
